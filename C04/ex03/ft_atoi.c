int	ft_atoi(char *str)
{
	int	x;
	int	res;
	int	sign;

	x = 0;
	res = 0;
	sign = 1;
	while (str[x] == ' ' || (str[x] >= 9 && str[x] <= 13))
		x++;
	while (str[x] != '\0' && (str[x] == '-' || str[x] == '+'))
	{
		if (str[x] == '-')
		{
			sign *= -1;
		}
		x++;
	}
	while (str[x] >= '0' && str[x] <= '9')
	{
		res = res * 10 + str[x] - '0';
		x++;
	}
	return (res * sign);
}
